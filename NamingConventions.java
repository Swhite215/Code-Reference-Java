public class NamingConventions {

    // Packages
    // ---- Always lower case
    // ---- Name should be unique
    // ---- Use internet domain name reversed as prefix for the name
    // ---- Example: java.lang

    // Classes
    // ---- CamelCase, Nouns, Capital Words
    // ---- Examples: ArrayList, LinkedList

    // Interfaces
    // ---- CamelCase, Nouns, Capital Words
    // ---- Examples: List, Comparable

    // Methods
    // ---- mixedCase, Verbs
    // ---- Examples: getName(), addPlayer()

    // Constants
    // ---- Uppercase, Separate words with underscore_
    // ---- Examples: MAX_INT, SEVERITY_ERROR

    // Variables
    // ---- mixedCase meaningful and indicative
    // ---- Examples: numOfPlayers, name

    // Type Parameters
    // ---- Single Character, Capital Letters
    // ---- Examples: E - Element, K - Key, T - Type, V - Value
}